function login(){
    document.getElementById("login").style.display = "block";
    document.getElementById("login_b").style.backgroundColor = "rgb(167, 167, 167)";
    document.getElementById("register").style.display = "none";
    document.getElementById("register_b").style.backgroundColor = "white";
}
function register(){
    document.getElementById("register").style.display = "block";
    document.getElementById("register_b").style.backgroundColor = "rgb(167, 167, 167)";
    document.getElementById("login").style.display = "none";
    document.getElementById("login_b").style.backgroundColor = "white";
}